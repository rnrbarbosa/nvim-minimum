local servers = { 
	'sumneko_lua',
	'ansiblels',
	'bashls',
	'dockerls',
	'gopls',
	'jsonls',
	'remark_ls',
	'pyright',
	'taplo',
	'yamlls',
}

for _, lsp in ipairs(servers) do
  nvim_lsp[lsp].setup {
    on_attach = on_attach,
    flags = {
      debounce_text_changes = 150,
    },
  }
end

